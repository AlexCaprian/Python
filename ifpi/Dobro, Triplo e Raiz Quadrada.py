#A variável “n” recebe, convertido para inteiro, a leitura do número que é feita pelo teclado.
n=int(input('Digite um número:'))
#Imprime na tela o dobro do número.
print(f'O dobro de {n} vale {n*2}.')
#Imprime na tela o triplo do número.
print(f'O triplo de {n} vale {n*3}')
#Imprime na tela o valor da raiz quadrada do número.
print(f'A raiz quadrada de {n} vale {n**(1/2):.2f}')