#A variável 'an' recebe um valor constante 1998
an=1998
#A variável 'aa' recebe um valor constante 2025
aa=2025
#A variável 'an2' recebe um valor constante 2021
an2= 2021
#A variável 'c1' recebe um valor do resultado da formula de calculo entre aa e an
c1=aa-an
#A variável 'c2' recebe um valor do resultado da formula de calculo entre aa e an2
c2=aa-an2
#imprime 'Quem nasceu em 1998, em 2025 terá 27.'
print(f'Quem nasceu em {an}, em {aa} terá {c1}.')
#imprime 'Quem nasceu em 2021, em 2025 terá 4.'
print(f'Quem nasceu em {an2}, em {aa} terá {c2}.')